    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     // Kontrollerar att inmatningsrutorna har värden
        if (Validering.textNamnHarVarde(fornamn, efternamn)) 
           try {
         //Deklarerar variabler
                 String enamn = efternamn.getText();
                 
                 String fnamn = fornamn.getText();
 
                 boolean ok = true;
              
                     // Hämta Lärar id för inmatat för och efternamn
                    String id = idb.fetchSingle("SELECT LARAR_ID FROM LARARE where fornamn=" + "'" + fnamn + "'" + "and efternamn=" + "'" + enamn + "'");
                     //Kontrollerar så att idet finns, om inte skickar felmeddelande  
                    if(id == null){ JOptionPane.showMessageDialog(null," Läraren finns inte");}
                     
                     else{
                        // Kontrollerar om läraren har listad kompetens och tar i så fall bort denna
                      String a = idb.fetchSingle("Select LARAR_ID from HAR_KOMPETENS_I where LARAR_ID="+"'"+ id+"'");
                      if(!(null ==a)) ok = false;{
                      idb.delete("delete from HAR_KOMPETENS_I WHERE LARAR_ID="+"'"+id+"'"); ok = true;} 
                      
                       
                       if( ok = true){
                       //Kontrollerar om läraren är ansvarig för en kurs, och skickar i så fall felmeddelande att borttag ej möjligt
                       String b = idb.fetchSingle("Select KURSLARARE from KURS where KURSLARARE="+"'"+ id+"'"); 
                       if(!(null ==b)) JOptionPane.showMessageDialog(null," Läraran ansvarar för en kurs välj ny kursansvarig innan borttag");
                            
                           
                   
    
                    if(b == null){ 
                       //Kontrollerar om läraren är föreståndare för ett elevhem och skickar i så fall felmeddelande att borttag ej möjligt
                       String c = idb.fetchSingle("Select FORESTANDARE from ELEVHEM where FORESTANDARE="+"'"+ id+"'"); 
                       if(!(null ==c)){ JOptionPane.showMessageDialog(null," Läraran är elevhemsföreståndare välj ny föreståndare innan borttag");}
                       
                      
                      
                    if( c == null){
                        //Tar bort läraren ut tabellen 
                     idb.delete("DELETE from LARARE where LARAR_ID=" +"'"+ id+"'");{JOptionPane.showMessageDialog(null,"  Läraren är nu borttagen");}}}}}
                     
                 } catch (InfException ex) {
                     Logger.getLogger(TaBortLärare.class.getName()).log(Level.SEVERE, null, ex);
                 }
                 
               
                     
                 
                     
                     
  
                  
                 
          
     
     
    }//GEN-LAST:event_jButton1ActionPerformed

